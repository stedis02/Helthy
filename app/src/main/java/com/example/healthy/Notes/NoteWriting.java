package com.example.healthy.Notes;

public class NoteWriting {
  private  String FileName;
  private  String FileWriting;
  private  String FileHeadWriting;

    public String getFileName() {
        return FileName;
    }

    public void setFileName(String fileName) {
        FileName = fileName;
    }

    public String getFileWriting() {
        return FileWriting;
    }

    public void setFileWriting(String fileWriting) {
        FileWriting = fileWriting;
    }

    public String getFileHeadWriting() {
        return FileHeadWriting;
    }

    public void setFileHeadWriting(String fileHeadWriting) {
        FileHeadWriting = fileHeadWriting;
    }


}
